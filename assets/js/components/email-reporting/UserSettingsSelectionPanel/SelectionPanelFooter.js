/**
 * External dependencies
 */
import PropTypes from 'prop-types';

/**
 * WordPress dependencies
 */
import { __ } from '@wordpress/i18n';

/**
 * Internal dependencies
 */
import Notice from '@/js/components/Notice';
import P from '@/js/components/Typography/P';

export default function SelectionPanelFooter( { notice, onNoticeDismiss } ) {
	return (
		<footer className="googlesitekit-selection-panel-footer">
			<div className="googlesitekit-selection-panel-footer__content">
				{ notice ? (
					<Notice
						type={ notice.type }
						description={ notice.text }
						dismissButton={ {
							label: __( 'Got it', 'google-site-kit' ),
							onClick: onNoticeDismiss,
						} }
						hideIcon
					/>
				) : (
					<P type="body" size="small">
						{ __(
							'This email is generated by Site Kit using data from your dashboard and sent to your WordPress email, so all your data stays with you',
							'google-site-kit'
						) }
					</P>
				) }
			</div>
		</footer>
	);
}

SelectionPanelFooter.propTypes = {
	notice: PropTypes.shape( {
		text: PropTypes.string,
		type: PropTypes.oneOf( [ 'info', 'success', 'error' ] ),
	} ),
	onNoticeDismiss: PropTypes.func,
};
